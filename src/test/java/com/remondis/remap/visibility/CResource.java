package com.remondis.remap.visibility;

class CResource {

  private String string;

  public CResource() {
    super();
  }

  public CResource(String string) {
    super();
    this.string = string;
  }

  public String getString() {
    return string;
  }

  public void setString(String string) {
    this.string = string;
  }

}
